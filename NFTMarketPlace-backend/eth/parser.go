func GenerateListID(nftAddr string, tokenID *big.Int) string {
	packed, _ := abi.Arguments{
		{Type: ContractAbi.Events["NFTListed"].Inputs[1].Type},
		{Type: ContractAbi.Events["NFTListed"].Inputs[2].Type},
	}.Pack(common.HexToAddress(nftAddr), tokenID)
	hash := crypto.Keccak256(packed)
	return "0x" + hex.EncodeToString(hash)
}

func ParseNFTListed(log types.Log) (*models.ListedNFT, error) {

	event, ok := ContractAbi.Events["NFTListed"]

	if !ok {
		return nil, fmt.Errorf("event NFTListed not found in ABI")
	}

	result, err := event.Inputs.NonIndexed().Unpack(log.Data)
	if err != nil {
		return nil, fmt.Errorf("failed to unpack NFTListed event: %w", err)
	}
	listIdBytes := result[0].([32]byte)
	listId := "0x" + common.Bytes2Hex(listIdBytes[:])
	price := result[1].(*big.Int)
	listedTime := result[2].(*big.Int)

	// 从索引参数中获取其他信息
	nftAddr := common.BytesToAddress(log.Topics[1].Bytes()).Hex()
	tokenId := new(big.Int).SetBytes(log.Topics[2].Bytes())
	seller := common.BytesToAddress(log.Topics[3].Bytes()).Hex()

	return &models.ListedNFT{
		ListID:     listId,
		NFTAddress: nftAddr,
		TokenID:    tokenId.String(),
		Price:      price.String(),
		ListedTime: listedTime.Int64(),
		Seller:     seller,
		ExpiredAt:  listedTime.Int64() + 30*24*3600, // default 30 days
		Status:     "listed",
	}, nil
}

func ParseNFTSold(log types.Log) (*models.Sale, string, error) {

	event, ok := ContractAbi.Events["NFTSold"]

	if !ok {
		return nil, "NFTSold event Parsed failed", fmt.Errorf("vent NFTSold not found in ABI")
	}

	result, err := event.Inputs.NonIndexed().Unpack(log.Data)
	if err != nil {
		return nil, "NFTSold event Parsed failed", fmt.Errorf("failed to unpack NFTSold event: %w", err)
	}

	listIdBytes := result[0].([32]byte)
	listId := "0x" + common.Bytes2Hex(listIdBytes[:])

	price := result[1].(*big.Int)
	soldTime := result[2].(*big.Int)

	seller := result[3].(common.Address).Hex()

	// 从索引参数中获取其他信息
	nftAddr := common.BytesToAddress(log.Topics[1].Bytes()).Hex()
	tokenId := new(big.Int).SetBytes(log.Topics[2].Bytes())
	buyer := common.BytesToAddress(log.Topics[3].Bytes()).Hex()

	return &models.Sale{
		ListID:     listId,
		NFTAddress: nftAddr,
		TokenID:    tokenId.String(),
		Price:      price.String(),
		SoldTime:   soldTime.Int64(),
		Seller:     seller,
		Buyer:      buyer,
	}, listId, nil
}

